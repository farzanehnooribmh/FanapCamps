public class Vehicle {
    private int speed;
    private boolean move;
    private boolean chanegear;

    public int getSpeed() {
        return speed;
    }

    public void setSpeed(int speed) {
        this.speed = speed;
    }

    public boolean isMove() {
        return move;
    }

    public void setMove(boolean move) {
        this.move = move;
    }

    public boolean isChanegear() {
        return chanegear;
    }

    public void setChanegear(boolean chanegear) {
        this.chanegear = chanegear;
    }
}
